<p>
    <br><b>2. Cách dạy chó con đi vệ sinh vào khay</b><br>
    Bạn nên chuẩn bị 2 cái khay vệ sinh cho chó con. Nên dùng khay to 1 chút và không có thành cao.
    <br>Dạy chó đi vệ sinh vào khay
    <br>Bên cạnh khay vệ sinh thì phải có thảm lót để thấm nước tiểu. Thảm mới mua các bạn thấm 1 chút nước tiểu vào rồi kẹp xuống dưới khay. Tác dụng của việc này là “đánh dấu khu vực có mùi nước tiểu của chó”. Để khi chó con buồn tè và ngửi thấy mùi thì sẽ tiểu vào đó.<br>
</p>