<p>
    <br><b>1. Cách huấn luyện chó con đi vệ sinh vào bồn cầu</b><br>
    Khi chó con đi vệ sinh bừa bãi, bạn có thể tỏ ra giận dữ hoặc dùng tay đánh nhẹ. Sau đó, bạn vệ sinh thật sạch bãi chiến trường mà chúng để lại vào bồn cầu.
    <br>Dạy chó đi vệ sinh trong bồn cầu
    Bạn xích chó vào một chỗ bắt chúng nằm im, sau một vài giờ thì dắt chó vào nhà vệ sinh chỗ bồn cầu để chúng tự ngửi thấy mùi phân của mình ở đó. Cần thực hiện nhiều lần để tạo thành thói quen, lâu dần cún cưng của bạn sẽ biết đi vệ sinh vào bồn cầu.<br>
</p>
